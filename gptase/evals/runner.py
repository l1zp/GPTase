"""EvalRunner: evaluates agent output quality using golden data.

Directory layout (flat, agent-centric):
    .claude/agents/{agent_name}/
      {agent_name}.md           # Agent definition
      evals/
        golden.yaml             # Golden data for this agent
        input.md                # Input file (optional)
        images/                 # Images (optional)
        output/                 # Cached output (optional, generated by live run)

Each agent has exactly one eval dataset.
"""

import json
import logging
from pathlib import Path
from typing import Optional
from typing import Tuple

import yaml

from gptase.evals.assertions import EvalResult
from gptase.evals.assertions import evaluate_key_facts
from gptase.evals.assertions import validate_schema
from gptase.utils.exceptions import AgentInitializationError

logger = logging.getLogger(__name__)

# Base directory for agent definitions
_CLAUDE_DIR = Path(__file__).resolve().parent.parent.parent / ".claude"
_AGENTS_DIR = _CLAUDE_DIR / "agents"


class EvalRunner:
    """Load golden data and evaluate agent outputs.

    Args:
        agent_name: Agent identifier (e.g., "vision-image-analyzer").
        config_path: Optional path to an llm_config JSON file to use for live
            runs instead of the default config. Supports the full llm_config
            format including agent_models overrides.
            Example: "config/llm_config.qwen_vl.example.json"
    """

    def __init__(
        self,
        agent_name: str,
        config_path: Optional[str] = None,
    ):
        self.agent_name = agent_name
        self.resolved_agent_name, self.evals_dir = self._resolve_agent_evals_dir(
            agent_name
        )
        self._config_path = config_path
        self.golden = self._load_golden()

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    async def eval_agent(self,
                         live: bool = False,
                         save_output: bool = False) -> EvalResult:
        """Evaluate the agent.

        Args:
            live: If True, run the agent live against the LLM API.
            save_output: If True, save live output to evals/output/.

        Returns:
            EvalResult for this agent.
        """
        if live:
            output, failure_reason, schema_error = await self._run_agent_live(
                save_output=save_output
            )
        else:
            output = self._load_cached_output()
            failure_reason = "cache_miss" if output is None else ""
            schema_error = (
                "No cached output available. Run with --live to generate output."
                if output is None else ""
            )

        if output is None:
            return EvalResult(
                agent_name=self.agent_name,
                schema_valid=False,
                schema_error=schema_error,
                total_facts=len(self.golden.get("key_facts", [])),
                passed_facts=0,
                failure_reason=failure_reason,
                failed_facts=["No output available"],
            )

        schema_name = self.golden.get("schema", "")
        schema_valid, schema_error = validate_schema(output, schema_name)

        return evaluate_key_facts(
            data=output,
            key_facts=self.golden.get("key_facts", []),
            agent_name=self.agent_name,
            schema_valid=schema_valid,
            schema_error=schema_error,
        )

    @staticmethod
    def _candidate_agent_names(agent_name: str) -> list[str]:
        """Return candidate agent names in the same order as Agent lookup."""
        return [agent_name, agent_name.replace("_", "-"), agent_name.replace("-", "_")]

    def _resolve_agent_evals_dir(self, agent_name: str) -> Tuple[str, Path]:
        """Resolve the canonical agent directory for eval assets."""
        for candidate in self._candidate_agent_names(agent_name):
            evals_dir = _AGENTS_DIR / candidate / "evals"
            if evals_dir.exists():
                return candidate, evals_dir
        return agent_name, _AGENTS_DIR / agent_name / "evals"

    # ------------------------------------------------------------------
    # Cache loading
    # ------------------------------------------------------------------

    def _load_cached_output(self) -> Optional[dict]:
        """Load the cached output from evals/output/."""
        output_dir = self.evals_dir / "output"
        if not output_dir.exists():
            logger.warning("[WARNING] No output directory: %s", output_dir)
            return None

        # Look for any JSON file in output/
        json_files = sorted(output_dir.glob("*.json"))
        if not json_files:
            logger.warning("[WARNING] No JSON files found in %s", output_dir)
            return None

        # Try to load the most recent file
        for json_path in reversed(json_files):
            try:
                with open(json_path, encoding="utf-8") as f:
                    return json.load(f)
            except (json.JSONDecodeError, OSError) as exc:
                logger.error("[ERROR] Failed to load %s: %s", json_path, exc)
                continue

        return None

    # ------------------------------------------------------------------
    # Live execution
    # ------------------------------------------------------------------

    def _build_model(self):
        """Build a Model instance, optionally loaded from a custom config file."""
        from gptase.models.model import Model

        if not self._config_path:
            return Model()

        config_file = Path(self._config_path)
        if not config_file.exists():
            raise FileNotFoundError(
                f"Config file not found: {config_file}\n"
                f"Pass a valid path, e.g. config/llm_config.qwen_vl.example.json"
            )

        with open(config_file, encoding="utf-8") as f:
            config_data = json.load(f)

        from gptase.utils.config import FrameworkConfig

        framework = FrameworkConfig(**config_data)
        model = Model(default_config=framework.to_model_config())
        model._framework_config = framework
        logger.info("[INFO] Eval using config: %s", config_file)
        return model

    async def _run_agent_live(
        self,
        save_output: bool = False,
    ) -> Tuple[Optional[dict], str, str]:
        """Run the agent live against the LLM API and return parsed JSON output.

        Reads input from evals/input.md or golden.yaml input_file field.
        """
        from gptase.agents.base import Agent

        # Get input text
        input_text = None
        input_file = self.golden.get("input_file")

        if input_file:
            input_path = self.evals_dir / input_file
            if input_path.exists():
                input_text = input_path.read_text(encoding="utf-8")
            else:
                logger.warning("[WARNING] Input file not found: %s", input_path)
        else:
            default_input = self.evals_dir / "input.md"
            if default_input.exists():
                input_text = default_input.read_text(encoding="utf-8")

        # Get images
        image_paths = None
        images_dir = self.evals_dir / "images"
        if images_dir.exists():
            exts = {".png", ".jpg", ".jpeg", ".gif", ".webp"}
            image_paths = [
                str(p) for p in sorted(images_dir.iterdir()) if p.suffix.lower() in exts
            ]

        if input_text is None and not image_paths:
            logger.error("[ERROR] No input available for live run")
            return None, "live_input_missing", "No input available for live run"

        try:
            model = self._build_model()
        except FileNotFoundError as exc:
            logger.error("[ERROR] %s", exc)
            return None, "live_model_config_missing", str(exc)

        try:
            agent = Agent.from_markdown(self.resolved_agent_name, model_manager=model)
        except AgentInitializationError as exc:
            logger.error("[ERROR] Agent definition not found: %s", self.resolved_agent_name)
            return None, "agent_init_error", str(exc)

        result = await agent.run(
            content=input_text or "Analyze the provided images.",
            image_paths=image_paths,
        )

        if result.get("status") == "error":
            logger.error("[ERROR] Agent failed: %s", result.get("error"))
            return None, "agent_runtime_error", (
                f"Agent failed: {result.get('error', 'unknown error')}"
            )

        # Save trajectory whenever the agent ran (before JSON parse, which may fail)
        if save_output:
            from datetime import datetime
            output_dir = self.evals_dir / "output"
            output_dir.mkdir(parents=True, exist_ok=True)
            timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
            trace_data = result.get("trace")
            if trace_data is not None:
                model_name = getattr(
                    getattr(agent, "model_config", None), "model_name", "unknown"
                )
                summary = {
                    "agent_name": self.resolved_agent_name,
                    "timestamp": timestamp,
                    "model": model_name,
                    "total_iterations": result.get("data", {}).get("iterations"),
                    "final_status": result.get("status"),
                    **{k: v for k, v in trace_data.items() if k != "steps"},
                }
                trace_file = output_dir / f"trace_{timestamp}.json"
                with open(trace_file, "w", encoding="utf-8") as f:
                    json.dump(
                        {"summary": summary, "steps": trace_data.get("steps", [])},
                        f,
                        indent=2,
                        ensure_ascii=False,
                    )
                logger.info("[INFO] Saved trace to: %s", trace_file)

        from gptase.utils.json_utils import parse_json_content

        content = result.get("data", {}).get("content", "")
        parsed = parse_json_content(content)
        if parsed is None:
            logger.error("[ERROR] Could not parse JSON from agent output")
            return None, "parse_error", "Could not parse JSON from agent output"

        # Save parsed output
        if save_output:
            output_file = output_dir / f"output_{timestamp}.json"
            with open(output_file, "w", encoding="utf-8") as f:
                json.dump(parsed, f, indent=2, ensure_ascii=False)
            logger.info("[INFO] Saved output to: %s", output_file)

        return parsed, "", ""

    # ------------------------------------------------------------------
    # Golden data loading
    # ------------------------------------------------------------------

    def _load_golden(self) -> dict:
        """Load golden.yaml for this agent.

        Returns:
            Parsed YAML as a dict (schema, key_facts, optional input_file).

        Raises:
            FileNotFoundError: If golden.yaml does not exist.
        """
        golden_path = self.evals_dir / "golden.yaml"
        if not golden_path.exists():
            raise FileNotFoundError(
                f"Golden file not found: {golden_path}\n"
                f"Create .claude/agents/{self.agent_name}/evals/golden.yaml"
            )
        with open(golden_path, encoding="utf-8") as f:
            return yaml.safe_load(f) or {}


async def run_eval(
    agent_name: str,
    live: bool = False,
    save_output: bool = False,
    config_path: Optional[str] = None,
) -> EvalResult:
    """Convenience function to run a single agent evaluation.

    Args:
        agent_name: Agent identifier.
        live: If True, run agent live against LLM API.
        save_output: If True, save live output.
        config_path: Optional path to an llm_config JSON file for live runs.

    Returns:
        EvalResult for this agent.
    """
    runner = EvalRunner(agent_name, config_path=config_path)
    return await runner.eval_agent(live=live, save_output=save_output)
